                     <div class="section">
                        <p>  Direktorat Jenderal Pendidikan Tinggi memperpanjang waktu pendaftaran Pertukaran Mahasiswa Merdeka hingga 2 Juli 2021. Melalui Program Pertukaran Mahasiswa Merdeka, para mahasiswa bisa merasakan belajar di universitas lain, bertujuan untuk membuka ruang pertemuan bagi mahasiswa untuk berjumpa, bercerita, dan berbagi. Target peserta untuk program ini adalah 20.000 mahasiswa .  

                        Program ini memberikan kesempatan pertukaran mahasiswa antarpulau. Kegiatan pertukaran pelajar akan dilakukan selama satu semester. Sesuai Kepmen Nomor 74/P/2021 yang memberikan jaminan pengakuan 20 SKS untuk kegiatan pembelajaran di program Kampus Merdeka, mahasiswa yang mengikuti program ini akan mendapatkan pembelajaran sebanyak 20 SKS yang dapat diikuti dengan tiga skema.

                        Skema pertama adalah 20 SKS penuh di universitas penerima secara luring. Skema kedua adalah 10 SKS di universitas penerima secara luring dan 10 SKS di universitas asal secara daring. Untuk skema ketiga, total 20 SKS bisa dilakukan secara kombinasi dengan 10 SKS di universitas penerima secara luring, dan 10 SKS untuk beberapa mata kuliah baik di universitas penerima mau pun universitas asal secara daring.

                        Meski demikian, kebijakan perpindahan mahasiswa akan disesuaikan dengan kebijakan penanganan pandemi Covid-19 oleh pemerintah pusat dan/atau daerah universitas penerima mau pun universitas asal. Mekanisme pertukaran secara daring sudah disiapkan sebagai alternatif program jika perpindahan secara luring dibatasi. </p>

