                        <h5>Kampus Mengajar Angkatan 1 Tahun 2021, Upaya Kemendikbud Libatkan Mahasiswa dalamPenguatan Pembelajaran di Sekolah Dasar pada Masa Pandemi </h5>
                    
                        <p>  Jakarta, 9 Februari 2021 ---Kampus Mengajar adalah kegiatan mengajar di sekolah yang merupakan bagian dari program Kampus Merdeka. Menteri Pendidikan dan Kebudayaan (Mendikbud), Nadiem Anwar Makarim menjelaskan tujuan diadakannya Kampus Mengajar adalah pertama, untuk menghadirkan mahasiswa sebagai bagian dari penguatan pembelajaran literasi dan numerasi. Kedua, membantu pembelajaran di masa pandemi, terutama untuk SD di daerah 3T. Penyelenggaraan program ini sendiri adalah atas dukungan Lembaga Pengelola Dana Pendidikan (LPDP). </p>
                        <p>“Tantangan yang kita hadapi sangatlah besar, khususnya bagi adik-adik kita yang duduk di bangku Sekolah Dasar. Melalui Kampus Mengajar 2021, saya ingin menantang adik-adik mahasiswa untuk juga mengatakan “SAYA MAU!” Yakni mau membantu mengubah tantangan tersebut menjadi harapan,” ungkap Mendikbud ketika meluncurkan program Kampus Mengajar secara daring di Jakarta, Selasa (9/2).</p>
                        <p>Lebih lanjut, Mendikbud juga mengajak teman-teman mahasiswa dari seluruh Indonesia untuk beraksi, berkolaborasi, dan berkreasi selama dua belas minggu untuk meningkatkan kualitas pembelajaran di Sekolah Dasar, terutama yang berada di daerah 3T, sekaligus mengasah kepemimpinan, kematangan emosional, dan kepekaan sosial.</p> 
                        <p>Di akhir sambutannya, Mendikbud juga menekankan agar perguruan tinggi dan dosen mendukung mahasiswanya untuk mengikuti program Kampus Merdeka dan mempermudah konversi sks, karena Kampus Merdeka adalah hak mahasiswa untuk belajar di luar kampus atau prodinya.</p>
                        <p>Melanjutkan Mendikbud, Direktur Utama Lembaga Pengelola Dana Pendidikan (LPDP), Kementerian Keuangan (Kemenkeu), Rionald Silaban, menyambut baik program yang digagas oleh Kemendikbud dalam kerangka program Kampus Merdekaini. “Besar harapan kami,para mahasiswa penerima beasiswaakan dapat memberikan kontribusi atas permasalahan nyata yang dihadapi dunia pendidikan akibat dari dampak pandemi COVID-19,” ujar Rionald ketika menyampaikan aspirasinyaakan program Kampus Mengajar, yang merupakan buah kerjasama antara Kemendikbud dan LPDP.</p>
